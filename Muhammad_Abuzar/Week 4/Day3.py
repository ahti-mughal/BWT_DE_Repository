# Numeric Data Types

# Integer
x = 5
print(type(x))

# Float
y = 2.5
print(type(y))

# Complex
z = 2 + 3j
print(type(z))

# Boolean Data Type

# True
x = True
print(type(x))

# False
y = False
print(type(y))

# Sequence Data Types

# List
my_list = [1, 2, 3, "four", True]
print(type(my_list))

# Tuple
my_tuple = (1, 2, 3, "four", True)
print(type(my_tuple))

# Set
my_set = {1, 2, 3, "four", True}
print(type(my_set))

# Dictionary
my_dict = {"name": "John", "age": 25, "is_student": True}
print(type(my_dict))

# Strings

# Single-line string
x = "Hello, World!"
print(type(x))

# Multi-line string
y = """Lorem ipsum dolor sit amet,
consectetur adipiscing elit,
sed do eiusmod tempor incididunt"""
print(type(y))

# Bytes and Byte Arrays

# Bytes
x = b"Hello"
print(type(x))

# Byte array
y = bytearray(5)
print(type(y))
